public class tugas1_bola {
    public int jariJari;
    
    public double hitungLuasbola(){
        double luasBola = 4 * 3.14 * jariJari * jariJari;
        return luasBola;
    }
    public double hitungVolumebola(){
        double volumeBola = 3.14 * jariJari * jariJari * jariJari * 4 / 3;
        return volumeBola;
    }
}
